# PART 2: DATA PREPROCESSING AND FEATURE ENGINEERING
# =============================================================================

import pandas as pd

df = pd.read_csv("LengthOfStay.csv")

# -------- rcount fix --------
df['rcount'] = df['rcount'].replace('5+', 5).astype(int)

# -------- encode gender --------
df['gender'] = df['gender'].map({'F':0, 'M':1})

# -------- encode facility --------
df = pd.get_dummies(df, columns=['facid'], drop_first=True)

# -------- create LOS category --------
def los_category(x):
    if x <= 3:
        return 0
    elif x <= 6:
        return 1
    else:
        return 2

df['los_cat'] = df['lengthofstay'].apply(los_category)

# -------- drop unused columns --------
df = df.drop(['eid','vdate','discharged','lengthofstay'], axis=1)

print(df.head())
print(df['los_cat'].value_counts())

df.to_csv("processed_los.csv", index=False)


