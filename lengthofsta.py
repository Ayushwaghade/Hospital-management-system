import pandas as pd
from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA
from sklearn.cluster import KMeans
import matplotlib.pyplot as plt
import seaborn as sns

# -----------------------
# LOAD DATA
# -----------------------
df = pd.read_csv("processed_los.csv")

X = df.drop("los_cat", axis=1)

# -----------------------
# SCALE DATA
# -----------------------
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# -----------------------
# PCA REDUCTION
# -----------------------
pca = PCA(n_components=2)
X_pca = pca.fit_transform(X_scaled)

print("Explained variance:", pca.explained_variance_ratio_)

# -----------------------
# KMEANS CLUSTERING
# -----------------------
kmeans = KMeans(n_clusters=3, random_state=42)
clusters = kmeans.fit_predict(X_scaled)

# -----------------------
# PLOT CLUSTERS
# -----------------------
plt.figure(figsize=(8,6))
sns.scatterplot(x=X_pca[:,0], y=X_pca[:,1], hue=clusters, palette="viridis", s=15)
plt.title("Patient Clustering (PCA + KMeans)")
plt.show()

# -----------------------
# COMPARE WITH TRUE LOS
# -----------------------
plt.figure(figsize=(8,6))
sns.scatterplot(x=X_pca[:,0], y=X_pca[:,1], hue=df['los_cat'], palette="Set1", s=15)
